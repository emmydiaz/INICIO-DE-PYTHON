#INGRESO DE DATOS POR TECLADO (IMPUT)

nombre = input("Ingrese su nombre: ")

#CONVERTIMOS A UNA VARIABLE DE TIPO INT
edad = int(input("Ingrese su edad: "))

#CONVERTIMOS A UNA VARIABLE DE TIPO FLOAT
talla = float(input("Ingrese su talla: "))

#MOSTRAMOS RESULTADOS
print(nombre)
print(edad)
print(talla)

#MOSTRAMOS EL TIPO DE DATO DE LAS VARIABLES
print(type(nombre))
print(type(edad))
print(type(talla))

